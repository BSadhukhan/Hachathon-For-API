package com.ninja.lms.dto;

public class UserDto {
	
	private String userId;
	private String userName;
	private long phoneNumber;
	private String location;
	private String timeZone;
	private String linkedinUrl;
	private String educationUG;
	private String educationPG;
	private String visaStatus;
	private String userComments;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public long getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(long phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getTimeZone() {
		return timeZone;
	}
	public void setTimeZone(String timeZone) {
		this.timeZone = timeZone;
	}
	public String getLinkedinUrl() {
		return linkedinUrl;
	}
	public void setLinkedinUrl(String linkedinUrl) {
		this.linkedinUrl = linkedinUrl;
	}
	public String getEducationUG() {
		return educationUG;
	}
	public void setEducationUG(String educationUG) {
		this.educationUG = educationUG;
	}
	public String getEducationPG() {
		return educationPG;
	}
	public void setEducationPG(String educationPG) {
		this.educationPG = educationPG;
	}
	public String getVisaStatus() {
		return visaStatus;
	}
	public void setVisaStatus(String visaStatus) {
		this.visaStatus = visaStatus;
	}
	public String getUserComments() {
		return userComments;
	}
	public void setUserComments(String userComments) {
		this.userComments = userComments;
	}
	
	
	
	
}
